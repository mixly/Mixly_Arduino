import turtle
import math

tina= turtle.Turtle()
tina.pencolor("#000000")
tina.pensize(5)
tina.penup()
tina.goto(-5,-5)
tina.pendown()
tina.fillcolor("#ffcc00")
tina.begin_fill()
tina.setheading(-50)
for i in range(0, 6, 1):
    tina.circle (45,150)
    tina.right(90)
tina.end_fill()
tina.penup()
tina.goto(0,0)
tina.setheading(0)
tina.pendown()
tina.fillcolor("#ff6600")
tina.begin_fill()
tina.circle (80,360)
tina.end_fill()
for i in range(-3, 4, 1):
    tina.penup()
    tina.goto(i * 20,math.fabs(i) * 10)
    tina.pendown()
    tina.setheading(90)
    tina.forward((160 - math.fabs(i) * 20))
for i in range(-3, 4, 1):
    tina.penup()
    tina.goto(-80 + math.fabs(i) * 10,80 + i * 20)
    tina.pendown()
    tina.setheading(0)
    tina.forward((160 - math.fabs(i) * 20))
tina.penup()
tina.goto(0,0)
tina.pendown()
tina.pencolor("#006600")
tina.setheading(-90)
tina.circle ((-600),20)
tina.penup()
tina.goto(-10,-100)
tina.setheading(-150)
tina.pendown()
tina.circle ((-60),120)
tina.setheading(30)
tina.circle ((-60),120)
tina.setheading(150)
tina.forward(100)
tina.penup()
tina.goto(-15,-150)
tina.setheading(70)
tina.pendown()
tina.circle ((-60),120)
tina.setheading(-110)
tina.circle ((-60),120)
tina.setheading(10)
tina.forward(100)
tina.hideturtle()
