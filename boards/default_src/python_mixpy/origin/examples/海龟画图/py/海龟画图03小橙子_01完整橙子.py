import turtle

tina= turtle.Turtle()
tina.pensize(5)
tina.fillcolor("#ff6600")
tina.begin_fill()
tina.right(180)
tina.circle (100,320)
tina.right(30)
tina.circle (45,100)
tina.end_fill()
tina.penup()
tina.goto(35,8)
tina.pendown()
tina.fillcolor("#330000")
tina.begin_fill()
tina.circle (5,360)
tina.end_fill()
tina.hideturtle()
