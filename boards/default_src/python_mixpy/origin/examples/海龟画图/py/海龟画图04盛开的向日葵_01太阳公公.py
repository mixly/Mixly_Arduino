import turtle

tina= turtle.Turtle()
tina.pencolor("#ffcc33")
tina.fillcolor("#ffcc33")
tina.pensize(5)
tina.begin_fill()
tina.circle (80,360)
tina.end_fill()
tina.penup()
tina.goto(-40,100)
tina.pendown()
tina.pencolor("#000000")
tina.setheading(30)
tina.circle ((-30),60)
tina.penup()
tina.goto(20,100)
tina.pendown()
tina.setheading(30)
tina.circle ((-30),60)
tina.penup()
tina.goto(-20,60)
tina.pendown()
tina.setheading(-30)
tina.circle (50,60)
tina.penup()
tina.goto(-30,-30)
tina.pendown()
tina.pencolor("#ffcc33")
tina.setheading(60)
for i in range(0, 12, 1):
    tina.circle ((-35),120)
    tina.left(150)
tina.hideturtle()
